    /*Retirar a Reserva da Lista*/
    private void btnCheckOutActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCheckOutActionPerformed
        /*Variáveis utilizadas*/
        cpf = txtCPFCheckOut.getText();
        int reservaPosicao;
        int hospedePosicao;
        /*Referencia das classes*/
        Hospede hospede;
        ReservaQuarto reserva;

        /*try..catch para capturar uma eventual exceção do tipo nullpointerexception*/
        try {
            /*Se o CPF estiver em branco*/
            if (cpf.equals("")) {
                throw new NullPointerException("Preencha o campo obrigatório!");
            }

            /*Se o CPF existe na lista de reservas*/
            if (listaReservas.exist(cpf)) {
                /*Captura o indice da reserva com esse CPF*/
                reservaPosicao = listaReservas.existReserva(cpf);
                /*Captura o objeto ReservaQuarto*/
                reserva = listaReservas.get(reservaPosicao);
                
                /*Mensagem de confirmação*/
                JOptionPane.showMessageDialog(null, "Check-out no CPF " + cpf + " efetuado com sucesso!");
                /*Mensagem para visualizar os serviços utilizados na conta do Hospéde*/
                JOptionPane.showMessageDialog(null, GerenciarContas.imprimirConta(cpf));

                /*Captura o indice do Hospede com esse CPF*/
                hospedePosicao = listaHospedes.existHospede(cpf);
                /*Captura o objeto Hospede*/
                hospede = listaHospedes.get(hospedePosicao);
                
                /*Método para visualizar os detalhes da Reserva e o Valor total a pagar*/
                visualizarCheckOut(hospede.getNomeCompleto(), hospede.getCpf(),
                        reserva.getTipoQuarto().getTipo_quarto(), reserva.getTipoQuarto().getQtd_adultos(),
                        reserva.getTipoQuarto().getQtd_crianca(), GerenciarContas.getValorTotal(cpf) + 
                                sqlQuarto.consultarPreco(reserva.getTipoQuarto().getTipo_quarto()));
                
                /*Método para remover o objeto ReservaQuarto da lista de reservas*/
                listaReservas.remove(reservaPosicao);
            } else {
                /*Exceção caso o CPF não tenha sido cadastrado ou já tenha feito o Check-Out*/
                throw new NullPointerException("CPF não encontrado ou Check-Out já efetuado!");
            }

        } catch (NullPointerException e) {
            /*Captura as mensagens de erro enviadas pelos exceptions*/
            JOptionPane.showMessageDialog(null, e.getMessage(), "Alerta", JOptionPane.INFORMATION_MESSAGE);
        }

    }//GEN-LAST:event_btnCheckOutActionPerformed
