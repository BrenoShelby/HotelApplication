    private void btnAddServicosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddServicosActionPerformed
        /*ArrayList para armazenar os checkboxs enviado pelo método*/
        ArrayList<JCheckBox> checkBoxs = arrayCheckBox();
        /*Referencia da classe servico e variável para armazenar o preco do servico*/
        Servico servico;
        double preco;
        
        /*try..catch para capturar uma eventual exceção do tipo nullpointerexception*/
        try {
            /*Armazena o CPF*/
            cpf = txtCpfSQ.getText();
            
            /*Se o CPF estiver em branco*/
            if (cpf.equals("")) {
                throw new NullPointerException("Preencha o campo obrigatório!");
            }
            
            /*Se o CPF existir na lista de reservas*/
            if (listaReservas.exist(cpf)) {
                /*Cria um novo objeto Conta com o CPF*/
                filaServico = new Conta(cpf);
                
                /*Percorre o ArrayList de checkboxs*/
                for (JCheckBox checkBox : checkBoxs) {
                    /*Se a checkbox estiver selecionada*/
                    if (checkBox.isSelected()) {
                        /*try..catch para capturar uma eventual exceção do tipo SQLException*/
                        try {
                            /*Armazena o preço vinda através de uma consulta no banco de dados*/
                            preco = sqlServico.consultaPreco(checkBox.getText());
                            
                            /*Cria uma novo objeto serviço*/
                            servico = new Servico();
                            
                            /*Seta o nome do serviço*/
                            servico.setNome(checkBox.getText());
                            /*Seta o preço*/
                            servico.setPreco(preco);
                            
                            /*Armazena o serviço em uma fila*/
                            filaServico.enqueue(servico);
                        } catch (SQLException e) {
                            /*Mensagem de erro caso o método consultaPreço lançe uma exceção*/
                            JOptionPane.showMessageDialog(null, "Erro na consulta de preço!\nErro: " + e.getMessage(), "Alerta", JOptionPane.INFORMATION_MESSAGE);
                        }
                    }
                }
                /*Adiciona a fila de servico em um arraylist*/
                GerenciarContas.adicionar(filaServico);
                /*Mensagem para visualizar os serviços selecionados*/
                JOptionPane.showMessageDialog(null, "===========Serviços==========\nCPF: " + cpf + filaServico.toString());
            } else {
                /*Exceção caso o CPF não exista na lista reservas*/
                throw new NullPointerException("CPF não possui nenhuma reserva cadastrada!");
            }

        } catch (NullPointerException e) {
            /*Captura a mensagem de erro enviada pelos exceptions*/
            JOptionPane.showMessageDialog(null, e.getMessage(), "Alerta", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnAddServicosActionPerformed
