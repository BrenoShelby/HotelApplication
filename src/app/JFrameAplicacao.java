    /*Cadastra o Hospéde*/
    private void btnCadHospedeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadHospedeActionPerformed
        /*Instanciando as classes que formam o Hospede*/
        Endereco endereco = new Endereco();
        Hospede hospede = new Hospede();

        /*Passando os parametros para o objeto endereco*/
        endereco.setNumero(txtNum.getText());
        endereco.setComplemento(txtComp.getText());
        endereco.setRua(txtRua.getText());
        endereco.setCidade(txtCidade.getText());
        endereco.setEstado(txtEstado.getText());
        endereco.setPais(txtPais.getText());

        /*Passando os parametros para o objeto hospede*/
        hospede.setNomeCompleto(txtNome.getText());
        hospede.setCpf(txtCpf.getText());
        hospede.setRg(txtRg.getText());
        hospede.setEmail(txtEmail.getText());
        hospede.setDataNasc(txtDataNasc.getText());
        hospede.setEndereco(endereco);
        hospede.setTelefoneCelular(txtTelCel.getText());
        hospede.setTelefoneResidencial(txtTelRes.getText());

        /*try..catch para tratar eventuais nullpointerexceptions*/
        try {
            /*Verificação dos campos obrigatórios*/
            if (hospede.getNomeCompleto() == null || hospede.getCpf() == null || hospede.getDataNasc() == null
                    || endereco.getRua() == null || endereco.getCidade() == null
                    || endereco.getEstado() == null || endereco.getPais() == null) {
                throw new NullPointerException("Preencha todos os campos obrigatórios!");
            }

            /*Se o CPF digitado já existir na listaHospedes lançará uma mensagem de aviso*/
            if (listaHospedes.exist(hospede.getCpf())) {
                throw new NullPointerException("CPF já cadastrado!");
            }

            /*try..catch para capturar uma eventual exceção lançada pela classe HospedeDAO*/
            try {
                /*Passa o objeto hospede para o banco de dados*/
                sqlHospede.inserir(hospede);
                /*Insere o objeto hospede numa lista*/
                listaHospedes.insert(hospede);
                /*Mensagem de sucesso*/
                JOptionPane.showMessageDialog(null, "Hospéde cadastrado com sucesso!");
                /*Caso não ocorra nenhum erro, este método limpa os campos de texto*/
                limparCadastroHospede();
            } catch (SQLException e) {
                /*Mensagem que indica o erro responsável pela exceção*/
                JOptionPane.showMessageDialog(null, "Erro na inserção de dados no sistema!\nErro --> " + e.getMessage());
            }

        } catch (NullPointerException e) {
            /*captura e exibe a mensagem do erro*/
            JOptionPane.showMessageDialog(null, e.getMessage(), "Alerta", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnCadHospedeActionPerformed
    /*Confirma a reserva*/
    private void btnConfirmarReservaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarReservaActionPerformed
        /* Classes utilizadas */
        ReservaQuarto rq = new ReservaQuarto();
        Quarto quarto = new Quarto();

        /*try..catch para capturar uma eventual exceção de nullpointerexception*/
        try {
            /* Variáveis para armazenar o item selecionado no combobox e converter para String*/
            tipoQuarto = (String) cbTipoQuarto.getSelectedItem();
            qtdAdultos = (String) cbQtdAdult.getSelectedItem();
            qtdCriancas = (String) cbQtdCrianc.getSelectedItem();

            /* Passando os parametros para o objeto quarto */
            quarto.setTipo_quarto(tipoQuarto);
            quarto.setQtd_adultos(qtdAdultos);
            quarto.setQtd_crianca(qtdCriancas);

            /* Passando os parametros para o objeto rq */
            rq.setCpf(txtCpfReserva.getText());
            rq.setTipoQuarto(quarto);
            rq.setDataSaida(txtDataEntrada.getText());
            rq.setDataEntrada(txtDataSaida.getText());

            /* Caso os valores passados para os atributos obrigatórios sejam null */
            if (("".equals(rq.getCpf()) || rq.getDataEntrada() == null || rq.getDataSaida() == null)) {
                /* Lançará uma exceção do tipo NullPointerException */
                throw new NullPointerException("Preencha todos os campos obrigatórios de maneira correta!");
            }

            /* try..catch para capturar uma eventual exception lançada pelo ReservaDAO*/
            try {
                /*Insere o objeto rq como parametro para a classe ReservaDAO*/
                sqlReserva.inserir(rq);
                /*Insere o objeto ReservaQuarto numa lista*/
                listaReservas.insert(rq);
                /*Caso não ocorra nenhum tipo de erro, esse método irá limpar os campos da Reserva*/
                limparReserva();
            } catch (SQLException e) {
                /*Mensagem de erro caso capture uma exceção vinda da ReservaDAO*/
                JOptionPane.showMessageDialog(null, "CPF inexistente, tente novamente!");
            }
        } catch (NullPointerException e) {
            /* Captura a exceção do tipo NullPointerException e retorna uma mensagem de erro */
            JOptionPane.showMessageDialog(null, e.getMessage(), "Alerta", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnConfirmarReservaActionPerformed

